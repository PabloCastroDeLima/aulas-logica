public class Animal {
    
   private String nome;
   private String idade;
   
   
   public Animal() {
    
    }
   
    
    public String getNome() {
        return this.nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public String getIdade() { 
        return this.idade;
    }  
    
    
    public void setIdade(String idade) {
        this.idade = idade;
    }
}