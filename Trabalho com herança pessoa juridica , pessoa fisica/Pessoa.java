public class Pessoa {
   
    private String nome;
    private double codigoDeRegistro;
    private String profissao;
    
    public Pessoa(){
    
    }
    
    public String getNome() {
        return this.nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public double getCodigoDeRegistro() {
        return this.codigoDeRegistro;
    }
    
    public void setCodigoDeRegistro(double codigoDeRegistro) {
        this.codigoDeRegistro = codigoDeRegistro;
    }
    
   public String getProfissao() {
        return this.profissao;
    }
    
    public void setProfissao(String profissao) {
        this.profissao = profissao;
    }
    

}